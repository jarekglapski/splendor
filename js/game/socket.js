function randomEl(list) {
  // getting random available position in given array
  var i = Math.floor(Math.random() * list.length);
  return list[i];
}

function randomName(el) {
  // interpolating two random strings in one:
  return `${randomEl(adjectives)} ${randomEl(nouns)}`;
}

function generateUserName() {
  // checking if _userId exists in localStorage
  if (localStorage.hasOwnProperty('_userId')) {
    // if yes, then returning existing _userId
    return localStorage.getItem('_userId');
  }

  // if not, generating new user name
  var userId = randomName();

  // and saving it to localStorage
  localStorage.setItem('_userId', userId);
  return userId;
}

